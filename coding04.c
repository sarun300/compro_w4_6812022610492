#include <stdio.h>

int main() {
    // กรณีที่ 1: ใช้ int
    int score1 = 90.5;  // จะเกิดการตัดทศนิยม
    printf("กรณีที่ 1 - int score: %d\n", score1);

    // กรณีที่ 2: ใช้ float
    float score2 = 90.5;
    printf("กรณีที่ 2 - float score: %.1f\n", score2);

    return 0;
}
// กรณีที่ 1: int score = 90.5;
// ผลลัพธ์ที่ได้: ค่าถูกตัดทศนิยมออก ทำให้ score กลายเป็น 90
// เหตุผล: เพราะกำหนดให้ตัวแปรเป็น int ซึ่งรองรับเฉพาะค่าจำนวนเต็ม การใช้ค่าทศนิยม (float/double) จะทำให้เกิดการตัดค่าทศนิยม หรือ error ขึ้นอยู่กับภาษา
// สรุปผล: ไม่ควรใช้ int เมื่อค่าที่ต้องการเก็บมีทศนิยม เพราะจะสูญเสียความแม่นยำของข้อมูล

// กรณีที่ 2: float score = 90.5;
// ผลลัพธ์ที่ได้: ค่าถูกเก็บเป็น 90.5 ตามที่ตั้งใจไว้
// เหตุผล: float รองรับค่าทศนิยมอยู่แล้ว จึงสามารถเก็บค่าดังกล่าวได้โดยไม่มีปัญหา
// สรุปผล: การเลือก Data Type ให้เหมาะสมกับประเภทข้อมูล เป็นสิ่งสำคัญ เพื่อไม่ให้เกิดความผิดพลาดหรือความคลาดเคลื่อนของข้อมูล