#include <stdio.h>   // Keyword + Header file สำหรับฟังก์ชัน printf

int main() {         // int = Keyword, main = Identifier, () = Operator
    printf("Hello,\n");     // printf = Identifier, "Hello,\n" = Constant
    printf("C Tokens!");    // แสดงข้อความอีกบรรทัด
    return 0;        // return = Keyword, 0 = Constant
}                     // {} = Operator ใช้กำหนดขอบเขตของ main
